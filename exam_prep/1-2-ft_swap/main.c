#include <stdio.h>

void	ft_swap(int *a, int *b);

int		main(void)
{
	int		*a;
	int		*b;
	int		aa;
	int		bb;

	aa = 5;
	bb = 15;
	a = &aa;
	b = &bb;
	printf("a = %d, b = %d\n", *a, *b);
	ft_swap(a, b);
	printf("a = %d, b = %d\n", *a, *b);
	return (0);
}
