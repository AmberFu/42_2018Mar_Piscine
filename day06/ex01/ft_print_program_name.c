void	ft_putchar(char str);

int		main(int argc, char **argv)
{
	int		i;

	i = 0;
	if (argc < 1)
		return (0);
	while (argv[0][i] != '\0')
	{
		ft_putchar(argv[0][i]);
		i++;
	}
	ft_putchar('\n');
	return (0);
}
