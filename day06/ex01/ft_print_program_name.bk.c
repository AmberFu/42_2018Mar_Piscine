void	ft_putchar(char str);

int		main(int argc, char **argv)
{
	int		i;

	i = 0;
	if (argc >= 0)
	{
		while (argv[0][i] != '\0')
		{
			ft_putchar(argv[0][i]);
			i++;
		}
	}
	return (0);
}
