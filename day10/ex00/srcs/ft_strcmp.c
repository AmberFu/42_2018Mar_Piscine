int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int ans;

	i = 0;
	ans = 0;
	if (s1[0] == '\0' && s2[0] != '\0')
		return (s1[0] - s2[0]);
	while (s1[i] != '\0')
	{
		if (s1[i] == s2[i])
			i++;
		else if (s2[i] == '\0')
			return (s1[i] - s2[i]);
		else
			return (s1[i] - s2[i]);
	}
	return (ans);
}
